char	*ft_strchr(const char *s, int c)
{
	char	cpy;

	cpy = (char)c;
	if (!s)
		return (0);
	while (*s != cpy && *s)
	{
		s++;
	}
	if (*s == cpy)
		return ((char *)s);
	else
		return (0);
}
